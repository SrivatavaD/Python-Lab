# This program prints the multiplication table of a number N
# using a while loop.

# Ask the user to enter a number.
n = int(input("Enter a number: "))

# Start the counter from 1.
i = 1

# Run the loop until 10.
while i <= 10:
    print(n, "x", i, "=", n * i)

    # Increase the counter by 1 each time.
    i += 1

